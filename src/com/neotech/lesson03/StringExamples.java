package com.neotech.lesson03;

public class StringExamples {

	public static void main(String[] args) {
		
		
		// primitive date type: name = value;
		// non primitive date type : String name = value; some character
		
		int a; // Declaring a but assigning 
		a = 9; // Assigning a value
		
		// date type     name      =   value
		String          shoolName = "Neotech";
		String name = "abit";
		int age = 40;
		String income = "50K";
		String nationality = "Turkish";
		
		//Reassigning 
		income = "120K";
		
		
		System.out.println(shoolName);
		System.out.println(name);
		System.out.println(income);
		System.out.println(age);
		System.out.println(nationality);

		
		
		




	}

}
